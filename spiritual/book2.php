    <h3> Book Summary </h3>
    <br>
<p>
<b>The Leader Who Had No Title</b> is the story of a <b>young army veteran</b>, returned to society and struggling to find meaning in his work and in his life.  Enter <b>Tommy Flinn</b>, an eccentric character who, over the course of a day, introduces our young hero to four unique individuals.  The mentor collective breathe life back into the vet as they <b>illuminate the path to meaningful work</b> and with it, a sense of purpose to life in general.
I think that last bit is the part that made this book so powerful for me.  I picked it up as a book on leadership and yet while it definitely provides tools and attitude shifts that will lead to better leadership in the workplace, the lessons in this book are about taking ownership of your life.  About not playing the victim.
