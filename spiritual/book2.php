<p>
<b>‘I Have A Dream’</b> is the story of <b>20 idealists</b> who think and act like entrepreneurs. They are committed to different causes, but they have one thing in common: a belief that principles of management can and must be used to achieve a greater common good.

These stories say one thing loud and clear change starts with one person, and that person could be<b> someone next door</b>. Someone like you.
For a generation thriving on relentless pursuit of selfish goals, Rashmi Bansal’s “I have a Dream” comes as a reminder that life is much more than just one’s own world. The book chronicles the lives of twenty people, who took a step towards bringing happiness in the lives of millions, but not in the usual charitable ways. They took the ideals of a business complete with profit motive and turned them into tools of betterment of the society. They are all, what can be termed as <b>‘social entrepreneurs’</b>.
